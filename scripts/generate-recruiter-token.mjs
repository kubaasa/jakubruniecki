const b64url = (input) =>
  Buffer.from(input)
    .toString("base64")
    .replace(/=+$/, "")
    .replace(/\+/g, "-")
    .replace(/\//g, "_");

const headerSegment = b64url(JSON.stringify(header));
const payloadSegment = b64url(JSON.stringify(payload));
const signingInput = `${headerSegment}.${payloadSegment}`;

const signatureSegment = createHmac("sha256", SECRET)
  .update(signingInput)
  .digest("base64")
  .replace(/=+$/, "")
  .replace(/\+/g, "-")
  .replace(/\//g, "_");

const token = `${signingInput}.${signatureSegment}`;

const here = dirname(fileURLToPath(import.meta.url));
const outPath = resolve(here, "..", "data", "recruiterToken.ts");

const moduleSource = `// AUTO-GENERATED by scripts/generate-recruiter-token.mjs — do not edit by hand.
// Re-run the script after changing the payload there.

export const RECRUITER_TOKEN =
  ${JSON.stringify(token)};
`;

writeFileSync(outPath, moduleSource, "utf8");

process.stdout.write(`Wrote ${outPath}\n${token}\n`);
